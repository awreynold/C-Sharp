using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator
{
	public static class CalcFunctions
	{
		public static int addFunction(int value1, int value2)
		{
			return value1 + value2;
		}

		public static int minusFunction(int value1, int value2)
		{
			return value1 - value2;
		}

		public static int multipleFunction(int value1, int value2)
		{
			return value1 * value2;
		}

		public static int divideFunction(int value1, int value2)
		{
			return value1 / value2;
		}
	}
}
